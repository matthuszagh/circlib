"""
Physical constants.
"""


def boltzmann():
    """
    Boltzmann constant in m^2 kg s^-2 K^-1
    """
    return 1.3806503e-23


def unit_charge():
    """
    Proton charge in C.
    """
    return 1.60217733e-19


def room_temperature():
    """
    Room temperature in K.
    """
    return 293.15
